#examples demonstrating floats
a = 10.5
b = 4.2

float_sum = a + b #addition
float_diff = a - b #subtraction
float_mult = a * b #multiplication
float_div = a / b #division
float_mod = a % b #modulus
float_power = a ** b #power of

print(f'Float sum: {float_sum}')
print(f'Float diff: {float_diff}')
print(f'Float mult: {float_mult}')
print(f'Float div: {float_div}')
print(f'Float mod: {float_mod}')
print(f'Float power: {float_power}')
