# Practical exercises and tasks for floats

def multiply_floats(a, b):
    return a * b

def divide_floats(a, b):
    return a / b

def sum_floats(a, b):
    return a + b

def subtract_floats(a, b):
    return a - b


print(multiply_floats(3.5, 2.6))
print(divide_floats(3.5, 2.0))
print(sum_floats(3.5, 2.6))
print(subtract_floats(3.5, 2.6))


