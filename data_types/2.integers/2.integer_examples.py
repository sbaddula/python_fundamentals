# various examples demonstrating the use of integers

a = 10
b = 3

int_sum = a + b #addition
int_diff = a - b #subtraction
int_mul = a * b #multiplication
floor_div = a // b #integer division
regular_div = a / b #division
modulus = a % b  #modulus or reminder
exponentiation = a ** b # a to the power of b


print("Sum: ", int_sum)
print("Diff: ", int_diff)
print("Multiplication: ", int_mul)
print("Floor Division: ", floor_div)
print("Regular Division: ", regular_div)
print("Modulus: ", modulus)
print("Exponentiation: ", exponentiation)










