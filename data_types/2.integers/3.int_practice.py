# Practical exercises and tasks for integers

def add_integers(a, b):
    return a + b

def sub_integers(a, b):
    return a - b

def mul_integers(a, b):
    return a * b

def div_integers(a, b):
    return a // b

print(add_integers(4, 5))
print(sub_integers(10, 5))
print(mul_integers(4, 5))
print(div_integers(10, 5))



